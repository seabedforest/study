"""
    while 循环计数
        循环以前 创建计数器
        循环条件 判断计数器是否满足条件
        循环以内 累加
    练习:exercise13

"""
# 抄5遍
count = 1
while count <= 5:# 1 ~ 5
    print("抄" + str(count) + "遍")
    count += 1  # 累加1

